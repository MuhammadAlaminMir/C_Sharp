using System;

namespace OOP_Theory
{
    class Program
    {
        static void Main(string[] args)
        {
            // Introduction to Object-Oriented Programming (OOP)

            // OOP is a programming paradigm based on the concept of objects.
            // It allows developers to create modular and reusable code by organizing data and behavior into classes and objects.
            // OOP is widely used in modern programming languages like C#, Java, Python, and C++.
            // It helps in modeling real-world entities and their interactions.
            // OOP is based on four main principles: Encapsulation, Inheritance, Polymorphism, and Abstraction.
            // These principles help in creating a clear structure for code, making it easier to understand and maintain.
            // OOP is essential for building complex software systems, as it allows for better organization and management of code.
            // Objects are instances of classes, which encapsulate data and behavior together.
            // OOP helps in organizing code, making it reusable, scalable, and easier to maintain.

            //OOP key concepts:
            // - Class: A blueprint for creating objects. It defines properties (fields) and methods (functions) that the objects will have.
            // - Object: An instance of a class. It represents a specific entity with its own state and behavior.
            // - Method: A function defined within a class that describes the behavior of the objects created from that class.
            // - Property: A variable defined within a class that represents the state of an object. It can be accessed and modified through methods.
            // - Constructor: A special method that is called when an object is created. It initializes the object's state.
            // - Destructor: A special method that is called when an object is destroyed. It is used to clean up resources. 
            

            // Key Principles of OOP:

            // 1. Encapsulation: Wrapping data (fields) and methods (functions) together into a single unit (class).
            //    It helps in data hiding and abstraction.

            // Real-life example: A car class with fields like speed, color, and methods like StartEngine() and StopEngine().

            // 2. Inheritance: A mechanism where one class can inherit properties and behavior from another class,
            //    promoting code reuse.

            // Real-life example: A Vehicle class inherited by Car and Bike classes.

            // 3. Polymorphism: The ability of a method to behave differently based on the object that invokes it.
            //    It can be achieved through method overloading and overriding.

            // Real-life example: A Shape class with a Draw() method, overridden by Circle and Rectangle classes.

            // 4. Abstraction: Hiding the complex implementation details and showing only the essential features of an object.

            // Real-life example: A coffee machine where you press a button to make coffee without knowing the internal mechanics.

            // Need for OOP:
            // OOP addresses the limitations of procedural programming by providing a modular structure.
            // It helps in managing complexity, especially in large software systems.
            // OOP promotes code reuse, reduces redundancy, and enhances maintainability.

            // Applications of OOP:
            // OOP is widely used in software development for building scalable and maintainable applications.
            // Examples include:
            // - Desktop applications (e.g., Microsoft Word, Excel)
            // - Web applications (e.g., e-commerce platforms)
            // - Mobile applications (e.g., Android and iOS apps)
            // - Game development (e.g., Unity-based games)
            // - Real-time systems (e.g., flight control software)
        }
    }
}